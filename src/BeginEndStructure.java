import java.util.TreeMap;
import java.util.regex.Pattern;

public class BeginEndStructure {

    protected enum Type{
        BODY,
        BEGIN,
        END
    }

    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//
    //!---------------------- CLASS ATTRIBUTES -----------------------!//
    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//
    
    protected Pattern pattern = null;

    protected TreeMap<Type,Group> group;

    protected String text = null;

    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//
    //!------------------------ CLASS METHODS ------------------------!//
    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//

    //*---------------------------------------------------------------*//
    //*-------------------------- CONSTRUCTORS -----------------------*//
    //*---------------------------------------------------------------*//

    public BeginEndStructure(){
    }

    //-----------------------------------------------------------------//

    public BeginEndStructure(String structureString){
        this.text = structureString; 
    }

    //*---------------------------------------------------------------*//
    //*---------------------------- SETTERS --------------------------*//
    //*---------------------------------------------------------------*//

    public void setText(String t) {
        text = t;
    }

    //-----------------------------------------------------------------//

    public void setPattern(Pattern p) {
        pattern = p;
    }

    //-----------------------------------------------------------------//

    public void setGroup(Type t, Group g) {
        group.put(t,g);
    }

    //*---------------------------------------------------------------*//
    //*---------------------------- GETTERS --------------------------*//
    //*---------------------------------------------------------------*//

    public String getText() {
        return text;
    }

    //-----------------------------------------------------------------//

    public Pattern getPattern() {
        return pattern;
    }

    //-----------------------------------------------------------------//

    public Group getGroup(Type t) {
        return group.get(t);
    }

    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//
    //!------------------- END OF BeginEndStructure ------------------!//
    //!---------------------------------------------------------------!//
    //!---------------------------------------------------------------!//
}
